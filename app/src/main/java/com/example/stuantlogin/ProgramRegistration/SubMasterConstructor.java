package com.example.stuantlogin.ProgramRegistration;

public class SubMasterConstructor {
    private String sSubID, sSubCode, sSubNamel;
    public SubMasterConstructor() {
    }

    public String getsSubID() {
        return sSubID;
    }

    public void setsSubID(String sSubID) {
        this.sSubID = sSubID;
    }

    public String getsSubCode() {
        return sSubCode;
    }

    public void setsSubCode(String sSubCode) {
        this.sSubCode = sSubCode;
    }

    public String getsSubNamel() {
        return sSubNamel;
    }

    public void setsSubNamel(String sSubNamel) {
        this.sSubNamel = sSubNamel;
    }
}
