package com.example.stuantlogin.ProgramRegistration;

public class ClassSubConstructor {

    private String sClassID, sSubID, sSubCode;

    public ClassSubConstructor() {
    }

    public String getsClassID() {
        return sClassID;
    }

    public void setsClassID(String sClassID) {
        this.sClassID = sClassID;
    }

    public String getsSubID() {
        return sSubID;
    }

    public void setsSubID(String sSubID) {
        this.sSubID = sSubID;
    }

    public String getsSubCode() {
        return sSubCode;
    }

    public void setsSubCode(String sSubCode) {
        this.sSubCode = sSubCode;
    }
}
